function I18N(type, string, replaceStr)
{
	var args = arguments;
	var res;
	
	string = string.toString() || '';
	res = LANG.LangReplace(args, string);

	if(replaceStr != null)
	{
		if(isNaN(replaceStr) == false)
		{
			res = res.replace(/%d/g, replaceStr);
		}
		else
		{
			res = res.replace(/%s/g, replaceStr);
		}
	}
	
	if (type === 'h') { document.write(res); }
	else if (type === 'j') { return res; }
}

